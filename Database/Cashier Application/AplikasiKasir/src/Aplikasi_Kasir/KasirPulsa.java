    private boolean checkString(String input, int lengthmin, int lengthmax) {
        if (input == null || (input.length() < lengthmin || input.length() > lengthmax)) {
            return false;
        }

        for (char c : input.toCharArray()) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }
    
    private Pulsa createPulsa(){
        String noTelp = fieldNoTelp.getText();
        String operator = fieldOperator.getText();
        float nominal = 0f;
        
        String kode = "";
        String nama = "";
        float harga = 0f;
        
        String sql = """
                     SELECT b.kode, b.nama, b.harga, p.nominal FROM barang b
                     INNER JOIN pulsa p ON p.kode = b.kode
                     WHERE p.kode = ?;
                     """;
        try {
            PreparedStatement stmt = DBConnector.conn.prepareStatement(sql);
            if (radioPulsa10.isSelected()){
                stmt.setString(1, "0008");
            }
            else if (radioPulsa50.isSelected()){
                stmt.setString(1, "0009");
            }
            else {
                stmt.setString(1, "0010");
            }
            
            ResultSet rs = stmt.executeQuery();
            if (rs.next()){
                nominal = rs.getFloat("nominal");
                kode = rs.getString("kode");
                nama = rs.getString("nama");
                harga = rs.getFloat("harga");
            }
        }
        catch (SQLException e){
            e.printStackTrace();
        }
        
        Pulsa temp = new Pulsa(operator,noTelp, nominal);
        temp.setKode(kode);
        temp.setHarga(harga);
        temp.setNama(nama);
        
        return temp;
    }
