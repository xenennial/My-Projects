    private void btnKonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfirmasiActionPerformed
        if (!checkString(fieldNoTelp.getText(), 10, 12)){
            JOptionPane.showMessageDialog(null, "Nomor HP tidak sesuai! Panjang harus diantara 10 - 12 angka, sekarang sebanyak " + fieldNoTelp.getText().length() + " angka dan tidak boleh diisi huruf/simbol.", "Invalid Input", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (seleksi_pulsa.getSelection() == null || seleksi_pembayaran.getSelection() == null){
            JOptionPane.showMessageDialog(null, "Tolong pilih jumlah pulsa dan/atau metode pembayaran!", "Invalid Input", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Pulsa temppulsa = createPulsa();
        MetodePembayaran new_payment;
        
        if (kasRadio.isSelected()) {
            new_payment = new Kas(temppulsa.getHarga());
        } else if (debitRadio.isSelected()) {
            new_payment = new KartuDebit(temppulsa.getHarga());
        } else {
            new_payment = new QRIS(temppulsa.getHarga());
        }
        
        new_payment.addItem(temppulsa, 1);
        new_payment.setFlag(MetodePembayaran.Flags.PULSA);
        new_payment.Payment();
        
        fieldNoTelp.setText("");
        fieldOperator.setText("");
    }//GEN-LAST:event_btnKonfirmasiActionPerformed

